; TODO: translate CPC colors. Use table and add #40
MACRO SETCOLOR color
        ld   a,{color}
        out  (c),a
ENDM
; -----------------------------------------


; -----------------------------------------
; Assign {color} to {ink}
MACRO SETINKCOLOR ink,color
        SELECTINK {ink}
        SETCOLOR {color}
ENDM
; -----------------------------------------


; -----------------------------------------
; Select next ink
MACRO SELECTNEXTINK
        inc c
        out  (c),c
ENDM
